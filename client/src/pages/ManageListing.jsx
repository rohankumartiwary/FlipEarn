import { Leaf, Loader2Icon, Upload, Verified } from 'lucide-react';
import React, { Children, useEffect, useState } from 'react'
import { useNavigate, useParams } from 'react-router-dom'
import { useSelector } from 'react-redux';
import toast from 'react-hot-toast';

const ManageListing = () => {
  const {id}=useParams();
  const navigate=useNavigate();
  const { userListings} = useSelector((state) => state.listing);
  const [loadingListing,setLoadingListing]=useState(false);
  const [isEditing,setIsEditing]=useState(false);
  // const isEditing = Boolean(id);


const [formData,setFormData]=useState({
    title:'',
    platform:'',
    username:'',
    followers_count:'',
    engagement_rate:'',
    monthly_views:'',
    niche:'',
    price:'',
    description:'',
    verified:false,
    monetized:false,
    country:'',
    age_range:'',
    images:[],
});

const platforms = [
  'youtube',
  'instagram',
  'facebook',
  'twitter (x)',
  'tiktok',
  'linkedin',
  'snapchat',
  'pinterest',
  'reddit',
  'discord',
  'telegram',
  'whatsapp channels',
  'twitch',
  'kick',
  'quora',
  'medium',
  'substack',
  'threads',
  'onlyfans',
  'patreon'
];

const niches = [
  'tech',
  'gaming',
  'education',
  'finance',
  'crypto',
  'stock market',
  'fitness',
  'health & wellness',
  'technology',
  'programming',
  'ai & automation',
  'digital marketing',
  'e-commerce',
  'fashion',
  'beauty & skincare',
  'travel',
  'food & cooking',
  'motivation',
  'podcasting',
  'music',
  'photography'
];

const ageRanges = [
  '18-34',
  '25-44',
  '25-54',
  '18-44',
  '55+',
  'mixed ages'
];


const handleInputChange=(feild,value)=>{
  setFormData((prev)=>({...prev,[feild]:value}));
}

const handleImageUpload=async(event)=>{
  const files=Array.from(event.target.files);
  if(!files.length) return;
  if(files.length + formData.images.length > 5) return toast.error("You can add upto 5 images");
  setFormData((prev)=>({...prev,images:[...prev.images,...files]}))
}

const removeImage=(indexToRemove)=>{
  setFormData((prev)=>({
    ...prev,images:prev.images.filter((_,i)=>i !== indexToRemove)
  }))
}

useEffect(()=>{
  // console.log("URL id:", id);
  // console.log("Redux listings:", userListings);

  if(!id) return;
  setIsEditing(true);
  setLoadingListing(true);
  const listing=userListings.find((listing)=>listing.id === id);
  if(listing){
    setFormData(listing);
    setLoadingListing(false);
  }else{
    toast.error("Listing not found");
    navigate("/my-listings");
  }
},[id]);

const handleSubmit=async(e)=>{
  e.preventDefault();
}

if(loadingListing){
  return (
    <div className='h-screen flex items-center justify-center'>
      <Loader2Icon className='size-7 animate-spin text-indigo-600'/>
    </div>
  )
}

  return (
    <div className='min-h-screen py-8'>
      <div className='max-w-4xl mx-auto px-4 sm:px-6 lg:px-8'>
        <div className='mb-8'>

        <h1 className='text-3xl font-bold text-gray-800'>
          {isEditing ? "Edit Listing" : "List Your Account"}
        </h1>
        <p className='text-gray-600 mt-2'>
          {isEditing ?
          'Update your existing account listing' : 
          'Create a mock listing to display your acccount info'}
        </p>
      </div>

      <form onSubmit={handleSubmit} className='space-y-8'>
        {/* Basic Info */}

        <Section title='Basic Information'>
            <div className='grid grid-cols-1 md:grid-cols-2 gap-6'>
              <InputFeild label="Listing Title" value={formData.title}
              placeholder='e.g.,Premium Travel Instagram Account'
              onChange={(v)=>handleInputChange('title',v)} required={true}
              />

              {console.log(formData)}
              <SelectFeild label='Platform'
              options={platforms}
              
              value={formData.platform}
              onChange={(v)=>handleInputChange('platform',v)}
              required={true}
              />

              <InputFeild label='Username/handle *' value={formData.username}
               onChange={(v)=>handleInputChange('username',v)}
               placeholder='@username'
               required={true}
               />

               <SelectFeild label='Niche/Category'
              options={niches}
              value={formData.niche}
              onChange={(v)=>handleInputChange('niche',v)}
              required={true}
              />
            </div>
        </Section>

        {/* Metrics */}

        <Section title="Account Metrics">
            <div className='grid grid-cols-1 md:grid-cols-3 gap-6 mb-6'>

              <InputFeild label="Followers Count"
              type='number'
              min={0}
              value={formData.followers_count}
              placeholder='10000'
              onChange={(v)=>handleInputChange('followers_count',v)}
              required={true}
              />

              <InputFeild label="Engagement Rate (%)"
              type='number'
              min={0}
              max={100}
              value={formData.engagement_rate}
              placeholder='10'
              onChange={(v)=>handleInputChange('engagement_rate',v)}
              required={true}
              />

              <InputFeild label="Monthly Views/Impressions"
              type='number'
              min={0}
              max={100}
              value={formData.monthly_views}
              placeholder='100000'
              onChange={(v)=>handleInputChange('monthly_views',v)}
              required={true}
              />
            </div>

            <div className='grid grid-cols-1 md:grid-cols-2 gap-6 mb-6'>

              <InputFeild label="Primary Audience Country"
                value={formData.country}
                placeholder='United States'
                onChange={(v)=>handleInputChange('country',v)}
              />

              <SelectFeild label='Primary Audience Age Range'
              options={ageRanges}
              value={formData.age_range}
              onChange={(v)=>handleInputChange('age_range',v)}
              />

            </div>

            <div className='space-y-3'>
              <CheckBoxField label="Account is verified on the platform"
                checked={formData.verified}
              />

              <CheckBoxField label="Account is Monetized"
              checked={formData.monetized}
              onChange={(v)=>handleInputChange('monetized',v)}
              />
            </div>
        </Section>

        {/* Pricing */}

        <section title="Pricing & Description">
            <InputFeild label='Asking Price(USD)'
            type='number'
            min={0}
            value={formData.price}
            placeholder='2500.0'
            onChange={(v)=>handleInputChange('price',v)}
            required={true}
            />

            <TextAreaField label="Description *"
            value={formData.description}
            onChange={(v)=>handleInputChange('description',v)}
            />
        </section>

        {/* images */}
        <Section title="ScreenShot & proof">
            <div className='border-2 border-dashed border-gray-300 rounded-lg p-6 text-center'>
              <input type="file" id='images' multiple accept='images/*'
                onChange={handleImageUpload}
                className='hidden'
              />

              <Upload className='w-12 h-12 text-gray-400 mx-auto mb-4'/>
              <label htmlFor="images"
              className='px-4 py-2 border border-gray-300 rounded-lg hover:bg-gray-50 cursor-pointer'
              >
               Choose Files 
              </label>
              <p className='text-sm text-gray-500 mt-2'>Upload screenshots or proof of account analytics</p>
            </div>
            {formData.images.length > 0 && (
              <div className='grid grid-cols-2 md:grid-cols-4 gap-4 mt-4'>
                {formData.images.map((img,index)=>(
                  <div key={index} className='relative'>
                    <img src={typeof img === 'string' ? img : URL.createObjectURL(img)} alt={`image ${index+1}`}
                    className='w-full h-24 object-cover rounded-lg'
                    />

                    <button
                    onClick={()=>removeImage(index)}
                    type='button'
                    className='absolute -top-2 -right-2 size-6 bg-red-600 text-white rounded-full hover:bg-red-700'
                    >
                     x
                    </button>
                  </div>
                ))}

              </div>
            )}
        </Section>

        <div className='flex justify-end gap-3 text-sm'>
            <button 
            type='button'
            className='px-6 py-2 border-gray-300 rounded-lg hover:bg-gray-50 transition-colors'
            >
              Cancel
            </button>

            <button
            type='submit'
            className='px-6 py-2 bg-indigo-600 text-white rounded-lg hover:bg-indigo-700 transition-colors'
            >
              {isEditing ? 'Update Listing' : 'Create Listing'}

            </button>
        </div>

      </form>
      </div>
      
    </div>
  )
}

const Section=({title,children})=>(
  <div className='bg-white rounded-lg border border-gray-200 p-6 space-y-6'>
    <h2 className='text-lg font-semibold text-gray-800'>{title}</h2>
    {children}
  </div>
)

const InputFeild=({label,value,onChange,placeholder,type="text",required=false,min=null, max=null})=>(
  <div>
    <label className='block text-sm font-medium text-gray-700 mb-2'>
      {label}
    </label>
    <input type={type} min={min} max={max} placeholder={placeholder} value={value}
    onChange={(e)=>onChange(e.target.value)}
    className='w-full px-3 py-1.5 text-gray-600 border rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500 border-gray-300'
    required={required}
    />
  </div>
)

const SelectFeild=({label,options,value,onChange,required=false})=>(
  <div>
    <label className='block text-sm font-medium text-gray-700 mb-2'>{label}</label>
      <select value={value}
        onChange={(e)=>onChange(e.target.value)}
        className='w-full px-3 py-1.5 text-gray-600 border rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500 border-gray-300'
        required={required}>
          <option value=''>Select.</option>
          {options.map((opt)=>(
            <option key={opt} value={opt}>{opt}</option>
          ))}
      </select>
  </div>
)

const CheckBoxField=({label,checked,onChange,required=false})=>(
  <label className='flex items-center space-x-2 cursor-pointer'>
    <input type="checkbox" checked={checked} onChange={(e)=>onChange(e.target.checked)}
    className='size-4'
    />

     <span className='text-sm text-gray-700'>
      {label}
     </span>
  </label>
)

const TextAreaField=({label,value,onChange,required=false})=>(
  <div>
    <label className='block text-sm font-semibold text-gray-700 mb-2'>{label}</label>
    <textarea rows={5} value={value} onChange={(e)=>onChange(e.target.value)}
    className='w-full px-3 py-1.5 text-gray-600 border rounded-md focus:outline-none focus:ring-2 focus:ring-indigo-500 border-gray-300'
    required={required}
    ></textarea>
  </div>
)
export default ManageListing;
